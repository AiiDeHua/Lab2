	private static boolean isHandFlush(ArrayList<Card> cards) {

		boolean bIsFlush = false;
		if (cards.get(eCardNo.FirstCard.getCardNo()).geteSuit() == cards.get(eCardNo.SecondCard.getCardNo()).geteSuit()
				&& cards.get(eCardNo.SecondCard.getCardNo()).geteSuit() == cards.get(eCardNo.ThirdCard.getCardNo())
						.geteSuit()
				&& cards.get(eCardNo.ThirdCard.getCardNo()).geteSuit() == cards.get(eCardNo.FourthCard.getCardNo())
						.geteSuit()
				&& cards.get(eCardNo.FourthCard.getCardNo()).geteSuit() == cards.get(eCardNo.FifthCard.getCardNo())
						.geteSuit()) {
			bIsFlush = true;
		}
		return bIsFlush;
	}

