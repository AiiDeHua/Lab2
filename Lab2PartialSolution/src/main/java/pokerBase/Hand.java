	public static boolean isHandRoyalFlush(Hand h, HandScore hs) {

		boolean isRoyalFlush = false;
		Card c = new Card();
		if ((h.getCardsInHand().get(eCardNo.FirstCard.getCardNo()).geteRank() == eRank.ACE)
				&& (isStraight(h.getCardsInHand(), c)) && (isHandFlush(h.getCardsInHand()))) {
			isRoyalFlush = true;
			hs.setHandStrength(eHandStrength.RoyalFlush.getHandStrength());
			// i don't know if it's correct but i set the hihand is zero since
			// people are absolutely win
			// if they get royal flush for now.(five of a kind)
			hs.setHiHand(0);
			hs.setLoHand(0);
			// and there is no kickers for royalflush
		}

		return isRoyalFlush;
	}

